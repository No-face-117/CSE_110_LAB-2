public class Intermediate_8 {
    public static void main(String[] args) 
    { 

    int a = 2;
    System.out.println("a = " + a);
    System.out.println();

    int b = 5;
    System.out.println("b = " + b);
    System.out.println();

    int c = 8;
    System.out.println("c = " + c);
    System.out.println();

    double d = ((2 * b) * ((c - a) / 3)) + 7 ;
    System.out.println("The value of d = " + d);
       
    }
}

